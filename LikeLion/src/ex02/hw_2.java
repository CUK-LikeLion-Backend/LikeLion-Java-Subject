package ex02;

public class hw_2 {
    public static void main(String[] args) {
        // 정수형 변수 선언
        byte a = 5;
        short b = 10;
        int c = 15;
        long d = 20;
        // 출력
        System.out.println("정수형 변수 : \nbyte = " + a + "\nshort = " + b + "\nint = " + c + "\nlong = " + d);

        // 실수형 변수 선언
        float e = 25;
        double f = 30;
        // 출력
        System.out.println("\n실수형 변수 : \nfloat = " + e + "\ndouble = " + f);

        // 문자열 변수 선언
        char g = 'A';
        // 출력
        System.out.println("\n문자열 변수 : \nchar = " + g );

        // 논리형 변수 선언
        boolean h = true;
        // 출력
        System.out.println("\n논리형 변수 : \nboolean = " + h );

    }
}
